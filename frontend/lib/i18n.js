export const translateCategory = (category, t) => {
  if (category === 'Bracelets') {
    return t.bracelets;
  }
  if (category === 'Necklaces') {
    return t.necklaces;
  }
  return category;
};

export const withVars = (template, vars = {}) =>
  Object.entries(vars).reduce((acc, [key, value]) => acc.replace(`{${key}}`, value), template);
